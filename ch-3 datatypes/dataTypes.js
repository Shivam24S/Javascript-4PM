// primitive data types

// 1.string example

let string = "abcd";

console.log(string);
console.log(typeof string);

// 2.Number

let num = 24;

console.log(num);
console.log(typeof num);

// 3.boolean

let result = true;

console.log(result);
console.log(typeof result);

// 4.undefined

let a;

console.log(a);
console.log(typeof a);

// 5.null

let reset = null;
console.log(reset);
console.log(typeof null);

// 6.symbol

let value = Symbol(20);
console.log(value);
console.log(typeof value);

// 7.bigInt

let bigNum = 900719925124740999n;

console.log(bigNum);
console.log(typeof bigNum);

// // non-permeative
// 8.object

let person = { name: "alice" };
console.log(person);
console.log(typeof person);

// array

let fruit = ["apple", "banana", "cherry"];

console.log(fruit);
console.log(typeof fruit);
