// multiple condition

let marks = 80;

if (marks >= 90) {
  console.log("you have achieved A grade");
} else if (marks >= 70) {
  console.log("you have achieved B grade");
} else if (marks >= 50) {
  console.log("you have achieved c grade");
} else if (marks >= 35) {
  console.log("you have achieved D grade");
} else {
  console.log("you have failed this examination");
}
